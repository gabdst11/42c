#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t	i;
	int		nb;
	int		sym;

	i = 0;
	sym = 1;
	nb = 0;
	while ((str[i] <= '\t' && str[i] >= '\r') || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sym *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	return (nb * sym);
}
